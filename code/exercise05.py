# Խնդիր 5։ Գրել ծրագիր, որը պահանջում է ներմուծել ամսվա 
#          անունը, ապա արտածում է տարվա այն եղանակի 
#          անունը, որի մեջ է տվյալ ամիսը։

month = input('Ամիսը. ')

if month in ['Մարտ', 'Ապրիլ', 'Մայիս']:
    print('Գարուն')
elif month in ['Հունիս', 'Հուլիս', 'Օգոստոս']:
    print('Ամառ')
elif month in ['Սեպտեմբեր', 'Հոկտեմբեր', 'Նոյեմբեր']:
    print('Աշուն')
elif month in ['Դեկտեմբեր', 'Հունվար', 'Փետրվար']:
    print('Ձմեռ')
else:
    print('Այդպիսի ամիս չկա')
